module.exports = function (x, y) {
  // Create a vector object.
  //
  // Parameters
  //   x
  //     a number. The translation along x-axis
  //   y
  //     a number. The translation along y-axis
  //
  // Return
  //   a vector2, a vector object { x, y }
  //
  return {
    x: x,
    y: y
  }
}
